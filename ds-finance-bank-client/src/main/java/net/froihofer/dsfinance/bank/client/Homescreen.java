    //Aktie kaufen -- FERTIG
    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        DefaultTableModel table = (DefaultTableModel) jTable3.getModel();

        if (jTable3.getSelectionModel().isSelectionEmpty()) {
            JOptionPane.showMessageDialog(this, "Es wurde keine Aktie ausgewählt", "Error", JOptionPane.ERROR_MESSAGE);
        } else {

            if (jTextField11.getText().equals("") || jTextField12.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Kein Textfeld darf leer sein", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (jTextField11.getText().matches("[0-9]+") && jTextField12.getText().matches("[0-9]+")) {

                int zeile = jTable3.getSelectedRow();

                try {

                    // Instead of doing all kinds of checks, we just extract the data from the form
                    // and pass it to the server: the server will buy stocks and create a transaction
                    // or it will signal an errors using a FinanceException.

                    int userId = Integer.parseInt(jTextField12.getText());
                    String companyName = (String) table.getValueAt(zeile, 0);
                    int totalNumber = Integer.parseInt(jTextField11.getText());
                    String symbol = table.getValueAt(zeile, 5).toString();

                    // This new method contains the business logic for buying and
                    // persisting transactions.
                    finance.buyStock(companyName, totalNumber, symbol, userId, BANK_ID);

                    jTextField11.setText("");
                    jTextField12.setText("");
                    JOptionPane.showMessageDialog(this, "Kauf erflogreich durchgeführt");

                } catch (FinanceException ex) {
                    // If an error was thrown, we show it to the user here
                    JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                    Logger.getLogger(Homescreen.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Die Textfelder dürfen nur Zahlen beinhalten", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

