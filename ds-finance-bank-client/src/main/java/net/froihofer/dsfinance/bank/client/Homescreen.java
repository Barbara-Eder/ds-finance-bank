    //Depot eines Kunden anzeigen -- FERTIG
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bitte eine Kundennummer eingeben", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (jTextField1.getText().matches("[0-9]+")) {
            DefaultTableModel table = (DefaultTableModel) jTable2.getModel();
            table.setRowCount(0);

            try {
                int userId = Integer.parseInt(jTextField1.getText());
                List<TransactionsDTO> result = finance.findDepotID(userId);

                if (result.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Keine Einträge im Depot des Kunden gefunden");
                } else {
                    for (int i = 0; i < result.size(); i++) {
                        //int userid = result.get(i).getUserId();
                        String companyname = result.get(i).getCompanyName();
                        String kurz = result.get(i).getSymbol();
                        BigDecimal tradedvalue = result.get(i).getTradedPrice();
                        int menge = result.get(i).getMenge();
                        BigDecimal gesamtwert = result.get(i).getGesamtWert();
                        BigDecimal currentvalue = finance.buyStock(kurz, menge);
                        int transactionid = result.get(i).getTransactionID();

                        // We also show the monthly average price here.
                        BigDecimal monthAvg = result.get(i).getMonatsDurchschnitt();
                        // Since it can happen that there were no trades in the last 30 days
                        // we check if the server returned the special value -1 i.e. missing.
                        // if we got no value here, we just set the cell to empty ""
                        String monthAvgStr = monthAvg.longValue() < 0 ? "" : monthAvg.toString();

                        table.addRow(new Object[]{companyname, kurz, tradedvalue, menge, gesamtwert, currentvalue, monthAvgStr, transactionid});
                    }

                    BigDecimal gesamtwertKunde = finance.getGesamtDepot(userId);
                    jTextField5.setText(gesamtwertKunde.toString());

                }
            } catch (FinanceException ex) {
                // We show the error message from the server to the user in a dialog
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(Homescreen.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Als Kundennummer sind nur zahlen erlaubt", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

